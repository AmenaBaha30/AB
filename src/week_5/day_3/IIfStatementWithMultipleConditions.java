package week_5.day_3;

public class IIfStatementWithMultipleConditions {

    public static void main(String[] args) {

        int number = 22;

        if ( number < 20 ) {
            System.out.println( number + " is less than 20" );
        } else if ( number > 22  ) {
            System.out.println( number + " is greater than 20" );
        } else {
            System.out.println( number + " is not greater or less than 20." );
        }

        System.out.println("Spartans Class!");

    }

}



