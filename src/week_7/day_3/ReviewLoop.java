package week_7.day_3;

public class ReviewLoop {

    public static void main(String[] args) {

        // Print numbers from 1 to 10
        // for ( initialization; condition; iteration ){
        //      // Body Of Loop
        // }
        for ( int number = 1; number <= 10; number++ ) {
            System.out.print( number + " " );
        }

    }

}
