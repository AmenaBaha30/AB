package week_13.day_1.abstraction;

public class Calculate {
    // Create a method to sum two int numbers and return the result
    // Create a method to sum three int numbers and return the result

    public int sum( int numberOne, int numberTwo ) {
        return numberOne + numberTwo;
    }
    public int sum( int numberOne, int numberTwo, int numberThree ) {
        return numberOne + numberTwo + numberThree;
    }

}
